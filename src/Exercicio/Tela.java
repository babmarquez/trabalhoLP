    public void addCorClienteCadeira(int cadeira, Color cor){
        switch(cadeira){
                case 1: 
                    pnCliente1.setBackground(cor);
                    break;
                case 2: 
                    pnCliente2.setBackground(cor);
                    break;
                case 3: 
                    pnCliente3.setBackground(cor);
                    break;
                case 4: 
                    pnCliente4.setBackground(cor);
                    break;
                case 5: 
                    pnCliente5.setBackground(cor);
                    break;
                case 6: 
                    pnCliente6.setBackground(cor);
                    break;
                case 0: 
                    pnDonaJoana.setBackground(cor);
                    break;
            }                 
    }
